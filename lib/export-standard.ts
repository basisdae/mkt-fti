/**
 * Shared Excel export standards for MKT HQ v1.0.
 * Read-only helpers — never write to DB or mutate business data.
 */
import type ExcelJS from "exceljs";
import { APP_VERSION } from "@/lib/constants";

export const EXPORT_GENERATED_BY = "MKT HQ";
export const EXPORT_VERSION_LABEL = "MKT HQ v1.0";

/** Soft header styling (readable, not flashy). */
export const EXPORT_HEADER_FILL = "FFF3F4F6";
export const EXPORT_HEADER_FONT = "FF1F2937";
export const EXPORT_ACCENT = "FF7A1F2B";

export interface ExportDateParts {
  datePart: string;
  timePart: string;
  display: string;
  iso: string;
}

export function getExportDateParts(date = new Date()): ExportDateParts {
  const y = date.getFullYear();
  const m = String(date.getMonth() + 1).padStart(2, "0");
  const d = String(date.getDate()).padStart(2, "0");
  const hh = String(date.getHours()).padStart(2, "0");
  const mm = String(date.getMinutes()).padStart(2, "0");
  return {
    datePart: `${y}${m}${d}`,
    timePart: `${hh}${mm}`,
    display: date.toLocaleString(),
    iso: date.toISOString(),
  };
}

export function sanitizeExportFileName(name: string, max = 40): string {
  const cleaned = name
    .trim()
    .replace(/[<>:"/\\|?*\u0000-\u001f]/g, "")
    .replace(/\s+/g, "_")
    .slice(0, max);
  return cleaned || "Untitled";
}

/** MKT_HQ_Product_Export_YYYYMMDD_HHMM.xlsx */
export function buildProductExportFileName(date = new Date()): string {
  const { datePart, timePart } = getExportDateParts(date);
  return `MKT_HQ_Product_Export_${datePart}_${timePart}.xlsx`;
}

